"""
Tipo None

O tipo  de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido tbm como o tipo vaizo,
porém falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos?

- Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de recebermos
um valor final.

OBS: O tipo None em Python é SEMPRE considerado como False
"""

numero = None

print(numero)
print(type(numero))

numero = 7j * 7j

print(numero)
print(type(numero))