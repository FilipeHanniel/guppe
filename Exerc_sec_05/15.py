print('DIAS DA SEMANA.')

dia = int(input('Digite um número de 1 a 7\n1 para domingo;2 para segunda e assim sucessivamente.\n'))

if dia == 1:
    print('DOMINGO')
elif dia == 2:
    print('SEGUNDA')
elif dia == 3:
    print('TERÇA')
elif dia == 4:
    print('QUARTA')
elif dia == 5:
    print('QUINTA')
elif dia == 6:
    print('SEXTA')
elif dia == 7:
    print('SÁBADO')
else:
    print('DIA INVÁLIDO')
